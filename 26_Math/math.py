# Python math

"""
The math module in Python provides many useful mathematical functions.
"""

# Built-in Math Functions

# The min() and max() functions can be used to find the lowest or highest value in an iterable.

x = min(5, 10, 25)
y = max(5, 10, 25)

print(x)
print(y)

print("------------------------------------------------")
####################################################################

# The abs() function returns the absolute value of a number.

x = abs(-7.25)

print(x)

print("------------------------------------------------")
####################################################################

# The pow(x, y) function returns the value of x to the power of y.

x = pow(4, 3)

print(x)

print("------------------------------------------------")
####################################################################

# The Math Module
# The math.sqrt() method for example, returns the square root of a number:
import math

x = math.sqrt(64)

print(x)

print("------------------------------------------------")
####################################################################

# The math.ceil() method rounds a number upwards to its nearest integer, 
# and the math.floor() method rounds a number downwards to its nearest integer, 
# and returns the result:

import math

x = math.ceil(1.4)
y = math.floor(1.4)

print(x)
print(y)

print("------------------------------------------------")
####################################################################

# The math.pi constant, returns the value of pi:

import math

x = math.pi

print(x)

